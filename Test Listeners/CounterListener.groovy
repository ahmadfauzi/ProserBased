class CounterListener {
	@BeforeTestSuite
    def beforeTestSuite(TestSuiteContext testSuiteContext) {
        // Ensure counter starts from Global Variable value
        if (GlobalVariable.counter == null) {
            GlobalVariable.counter = 1
        }
    }

    @BeforeTestCase
    def beforeTestCase(TestCaseContext testCaseContext) {
        // Read the current counter value
        int counter = GlobalVariable.counter
        
        // Increment counter and reset if reaching 999
        counter = (counter >= 999) ? 1 : counter + 1
        
        // Store the updated counter value
        GlobalVariable.counter = counter
    }
}